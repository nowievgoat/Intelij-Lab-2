// Поле 1 - Ток в амперах , Поле 2 - Сопротивление резистора R1(в Омах) , Функция - Вычислить мощность на участке электрической цепи (в Ваттах)

class ElectricCircuit(private var current: Double, private var resistance: Double) {

    // Конструктор для инициализации объекта
    init {
        // Можно добавить проверки на корректность входных данных, например, что ток и сопротивление неотрицательны
    }

    // Функция формирования строки с информацией об объекте
    fun getInfoString(): String {
        return "Ток: $current А, Сопротивление: $resistance Ом"
    }

    // Функция для вычисления мощности на участке электрической цепи (в Ваттах)
    fun calculatePower(): Double {
        return current * current * resistance
    }
}

fun main() {
    // Пример использования класса в основной программе
    val electricCircuit = ElectricCircuit(5.0, 10.0)

    // Вывод информации о объекте в Memo (предполагается, что Memo - это некий вывод текста)
    println(electricCircuit.getInfoString())

    // Вычисление мощности и вывод результата
    val power = electricCircuit.calculatePower()
    println("Мощность на участке электрической цепи: $power Ватт")
}
